var map;
resetMap();

function generateLevelTwoMap() {
  resetMap();

  map[1][1] = 2; // starting position for player
  var i = 1;
  var j = 1;
  var totalSpots = map.length + map[0].length - 4;

  while (i < map.length - 2 && j < map[0].length - 2) {
    var rand = Math.floor(Math.random() * totalSpots);
    // if rand is 1 increase i, if rand is 0 increase j
    i += rand < map.length - 2 ? 1 : 0;
    j += rand >= map.length - 2 ? 1 : 0;
    map[i][j] = 3;
  }

  map[i][j] = 4;
  map[i + 1][j] = map[i + 1][j] != 0 ? map[i + 1][j] : 4;
  map[i - 1][j] = map[i - 1][j] != 0 ? map[i - 1][j] : 4;
  map[i][j + 1] = map[i][j + 1] != 0 ? map[i][j + 1] : 4;
  map[i][j - 1] = map[i][j - 1] != 0 ? map[i][j - 1] : 4;
}

function generateLevelOneMap() {
  resetMap();
  for (var y = 1 ; y < map.length/2 + 1; y++) {
    map[y][map[0].length/2 - 1] = 3;
  }
  map[map.length/2][map[0].length/2] = 3;
  for (var y = map.length/2 ; y < map.length - 1 ; y++) {
    map[y][map[0].length/2 + 1] = 3;
  }
  map[map.length-2][map[0].length/2] = 4;
  map[map.length-2][map[0].length/2 + 1] = 4;
  map[map.length-2][map[0].length/2 + 2] = 4;
}

